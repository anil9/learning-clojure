(ns learning-clojure.advent-of-code.2021.day8-test
  (:require [clojure.test :refer :all])
  (:require [learning-clojure.advent-of-code.2021.day8 :refer [disj-all]]))

(deftest disj-all-test)
  ;(is (= (disj-all (set "abcdefg") "ab") (set "cdefg"))))
  ;(is (= (disj-all (set "abcdefg") ["c" "fg"]) (set "abde")))
  ;(is (= (disj-all (set "abcdefg") "ab" ["c" "fg"]) (set "de")))
  ;(is (= (disj-all (set "abcdefg") '("c" "fg") "ab" "d") (set "e"))))
